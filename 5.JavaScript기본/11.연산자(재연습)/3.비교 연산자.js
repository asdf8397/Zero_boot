/** 비교연산자
 * 이상 / 초과
 * 이하 / 미만
 * 대상 < 비교할 값
 */

const age = 20;


if (age < 10) {
    console.log("age가 10보다 작을땐 출력")
    /* age가 10미만일땐 */
} else {
    console.log("age가 10보다 크면 출력")
    /* age가 10 초과할때 */
}

if (age > 10) {
    console.log("age가 10보다 작을땐 출력")
    /* age가 10미만일때 */
} else {
    console.log("age가 10보다 클땐 출력")
    /* age가 10 초과할때 */
}
