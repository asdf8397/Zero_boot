/** DOM선택
 *
 * Document.getElementById();
 * Document.getElementById();는 JS에서 HTML태그의 id를 가져와서
 * JS에서 사용할 수 있음
 *
 * 
 * Document.getElementByClassName()
 * Element의 메소드 getElementsByClassName()은 주어진 클래스를 가진 모든
 * 자식 element의 실시간 HTMLCollection을 반환합니다.
 * 
 * HTML에서 class의 name으로 지정한 값을 js에서 가져와서 변형을 주거나 효과를
 * 줘서 사용할 수 있음
 * 
 * 
 * Document.getElementsByTagName()
 * Element의 메소드를 getElementsByTagName()으로 주어진 
 * <h1></h1>, <div></div>로 다룰수 있고 사용할 수 있음
 *
 * 프로젝트, 코딩테스트에서 사용될 수 있음,,, 그리고 HTML과 JS를 연결할때 많이 사용함
 * 특히 JS에서 HTML의 기능을 넣을때 많이 사용한다 알아둘 것
 * document.querySelector, document.querySelectorAll
 * 
 */