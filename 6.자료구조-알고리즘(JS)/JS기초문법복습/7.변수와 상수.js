/* 7. 변수와 상수 */
// 변수: 변경 가능한 값을 저장하기 위한 기억공간(Memory)
// 키워드: let

let a = 123;
console.log(a);

a = 789;
console.log(a);


// 상수
// 상수는 변경 불가능
// 키워드: const

const Restart = 777;
console.log(Restart);


let hi;
hi = "Hello!!!!";

console.log(hi);

// 한 줄에 여러 변수 선언과 초기화
// let 또는 const 변수선언할때 여러가지를 동시에 한번에 let const 변수에서 지정해서 넣어줄 수 있다.

let name1 = "김건우", age = 23, NickName = "Ben";
console.log(name1);
console.log(age);
console.log(NickName);